package com.seocks.old.model;

public class SeocksBean {
	
	private String pno;
	private String pname;
	private int pprice;
	private String pinfo;
	private String pmainimg;
	private String psubimg;
	private int pcount;
	private int pcate;
	
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPprice() {
		return pprice;
	}
	public void setPprice(int pprice) {
		this.pprice = pprice;
	}
	public String getPinfo() {
		return pinfo;
	}
	public void setPinfo(String pinfo) {
		this.pinfo = pinfo;
	}
	public String getPmainimg() {
		return pmainimg;
	}
	public void setPmainimg(String pmainimg) {
		this.pmainimg = pmainimg;
	}
	public String getPsubimg() {
		return psubimg;
	}
	public void setPsubimg(String psubimg) {
		this.psubimg = psubimg;
	}
	public int getPcount() {
		return pcount;
	}
	public void setPcount(int pcount) {
		this.pcount = pcount;
	}
	public int getPcate() {
		return pcate;
	}
	public void setPcate(int pcate) {
		this.pcate = pcate;
	}
	
	
	
}
